
public class VisitedNode {
	
	int f;
	String md5;
	
	public VisitedNode(int f, String md5) {
		this.f = f;
		this.md5 = md5;
	}
	
	public int getF() {
		return f;
	}
	public void setF(int f) {
		this.f = f;
	}
	public String getMd5() {
		return md5;
	}
	public void setMd5(String md5) {
		this.md5 = md5;
	}
}
