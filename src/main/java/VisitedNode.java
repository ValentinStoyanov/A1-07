
public class VisitedNode {
	
	double f;
	String md5;
	
	public VisitedNode(double f, String md5) {
		this.f = f;
		this.md5 = md5;
	}
	
	public double getF() {
		return f;
	}
	public void setF(double f) {
		this.f = f;
	}
	public String getMd5() {
		return md5;
	}
	public void setMd5(String md5) {
		this.md5 = md5;
	}
}
